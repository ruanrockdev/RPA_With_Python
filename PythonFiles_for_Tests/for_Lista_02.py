# Usando for pra exibir lista sem variavel
# Passando os elementos da lista diretamente
print("Usando for com lista sem variavel da lista")
for i in [0, 1, 2, 3, 4]:
    print(i)

print("\n")

print("Usando for com lista da variavel da lista")
lista = ['q', 'w', 'e', 'r', 't']
print("Mostrando itens da lista na mesma linha:", lista)
print("Mostrando itens da lista com 1 em cada linha:")
for i in lista:
    print(i)

l = [5, 3, 6, 4]
print(l * 2)

n = ['teste1', 'teste2', 'teste3']
print(n * 2)
